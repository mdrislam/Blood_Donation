package com.mristudio.blooddonation.model;

public class District_Name {
    private String id;
    private String division_id;
    private String name;
    private String bn_name;
    private String url;
    private String lat;
    private String lon;

    public District_Name() {
    }

    public String getId() {
        return id;
    }

    public String getDivision_id() {
        return division_id;
    }

    public String getName() {
        return name;
    }

    public String getBn_name() {
        return bn_name;
    }

    public String getUrl() {
        return url;
    }

    public String getLat() {
        return lat;
    }

    public String getLon() {
        return lon;
    }
}
